int	ft_isalnum(unsigned char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 48 && str[c] <= 57)
			|| (str[c] >= 65 && str[c] <= 90)
			|| (str[c] >= 97 && str[c] <= 122))
			c++;
		else
			return (1);
	}
	return (0);
}
